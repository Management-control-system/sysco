/**
 * One-time data uploading (Migration from localStorage to Cloud Firestore).
 * NOTE: PINs are intentionally NOT migrated here — they must never be
 * written to Firestore directly from the client (see firestore.rules).
 * Staff PINs created while offline should be re-created via the Security
 * Settings screen once the school is connected to the cloud, which goes
 * through the backend's hashing endpoint instead.
 */
export async function uploadLocalDataToFirebase(schoolId: string, students: Student[], teachers: Teacher[]) {
  if (!db) return false;
  try {
    const batch = writeBatch(db);
    
    // Migrate students
    for (const s of students) {
      const docRef = doc(db, 'schools', schoolId, 'students', s.id);
      batch.set(docRef, s);
    }
    
    // Migrate teachers
    for (const t of teachers) {
      const docRef = doc(db, 'schools', schoolId, 'teachers', t.id);
      batch.set(docRef, t);
    }

    await batch.commit();
    return true;
  } catch (error) {
    handleFirestoreError(error, OperationType.WRITE, `schools/${schoolId}/batch-migration`);
    return false;
  }
}
